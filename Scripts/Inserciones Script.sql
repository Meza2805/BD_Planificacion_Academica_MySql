						inner join MODALIDAD M ON GG.ID_MODALIDAD = M.ID
                        INNER JOIN GRADO G ON GG.ID_GRADO = G.ID
                        INNER JOIN TURNO T ON GG.ID_TURNO = T.ID
                        INNER JOIN SECCION S ON GG.ID_SECCION = S.ID;
                        
